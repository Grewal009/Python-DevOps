
def addition(a,b):
    add = a + b
    return add

def subtraction(a,b):
    sub = a - b
    return sub

def multiplication(a,b):
    mul = a * b
    return mul

def division(a,b):
    div = a/b
    return div

print("sum:",addition(10,3))
print("sub:",subtraction(11,1))
print("mul:",multiplication(221,2))
print("div:",division(123,10))